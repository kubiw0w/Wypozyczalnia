  CREATE OR REPLACE EDITIONABLE TRIGGER "PROJEKT"."TRG_INSERT_RAPORT" 
AFTER INSERT ON WYPOZYCZENIA
FOR EACH ROW
DECLARE
    v_imie KLIENCI.IMIE%TYPE;
    v_nazwisko KLIENCI.NAZWISKO%TYPE;
    v_email KLIENCI.EMAIL%TYPE;
    v_telefon KLIENCI.TELEFON%TYPE;
    v_numer_klienta KLIENCI.NUMER_KLIENTA%TYPE;

    v_marka SAMOCHODY.MARKA%TYPE;
    v_model SAMOCHODY.MODEL%TYPE;
    v_rok_produkcji SAMOCHODY.ROK_PRODUKCJI%TYPE;
    v_nr_rejestracyjny SAMOCHODY.NR_REJESTRACYJNY%TYPE;
BEGIN
    -- Pobierz dane klienta
    SELECT IMIE, NAZWISKO, EMAIL, TELEFON, NUMER_KLIENTA
    INTO v_imie, v_nazwisko, v_email, v_telefon, v_numer_klienta
    FROM KLIENCI
    WHERE ID_KLIENTA = :NEW.ID_KLIENTA;

    -- Pobierz dane samochodu
    SELECT MARKA, MODEL, ROK_PRODUKCJI, NR_REJESTRACYJNY
    INTO v_marka, v_model, v_rok_produkcji, v_nr_rejestracyjny
    FROM SAMOCHODY
    WHERE ID_SAMOCHODU = :NEW.ID_SAMOCHODU;

    -- Wstaw do RAPORTY
    INSERT INTO RAPORTY (
        ID_RAPORTU, IMIE, NAZWISKO, EMAIL, TELEFON, NUMER_KLIENTA,
        MARKA, MODEL, ROK_PRODUKCJI, NR_REJESTRACYJNY,
        DATA_WYPOZYCZENIA, DATA_ZWROTU
    ) VALUES (
        RAPORTY_SEQ.NEXTVAL, v_imie, v_nazwisko, v_email, v_telefon, v_numer_klienta,
        v_marka, v_model, v_rok_produkcji, v_nr_rejestracyjny,
        :NEW.DATA_WYPOZYCZENIA, :NEW.DATA_ZWROTU
    );
END;

